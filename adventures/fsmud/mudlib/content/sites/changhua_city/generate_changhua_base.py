def write(path, content):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    if os.path.exists(path):
        print(f"  SKIP: {os.path.basename(path)}")
        return
    with open(path, "w", encoding="utf-8") as f:
        f.write(content)
    print(f"  OK: {os.path.basename(path)}")

def overwrite(path, content):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        f.write(content)
    print(f"  OW: {os.path.basename(path)}")

def site(sid, cname, heritage, desc, travel, names_list, conns, rumors=None, hidden=False, extra=""):
    names_yaml = "\n".join(f"  - era: {e}\n    name: {n}" for e, n in names_list)
    conns_yaml = "\n".join(f"  - {c}" for c in conns)
    rumors_yaml = ("\nrumors:\n" + "\n".join(f'  - "{r}"' for r in rumors)) if rumors else ""
    hidden_line = "hidden: true\n" if hidden else ""
    desc_indented = "\n  ".join(desc.strip().splitlines())
    return f"""id: {sid}
settlement: changhua_city
canonical_name: {cname}
is_heritage: {str(heritage).lower()}
{hidden_line}base_description: |
  {desc_indented}
travel_arrive_text: "{travel}"

names:
{names_yaml}
{rumors_yaml}
connections:
{conns_yaml}
{extra}"""

